public abstract class Torre {

    //attributi
    protected float x;
    protected float y;
    protected float tempoRicaricaSparo;
    
    Torre(float x, float y, float tempoRicaricaSparo) {
        this.x = x;
        this.y = y;
        this.tempoRicaricaSparo = tempoRicaricaSparo;
      }
    
}